namespace PolishFootballNetwork.Domain.Enums;

/// <summary>
/// Represents the different league types in Polish football system.
/// </summary>
public enum LeagueType
{
    /// <summary>
    /// The top division of Polish football league system.
    /// </summary>
    Ekstraklasa = 1,

    /// <summary>
    /// The second division of Polish football league system (formerly known as I Liga).
    /// </summary>
    Fortuna1Liga = 2,

    /// <summary>
    /// European clubs that are not part of the Polish league system.
    /// </summary>
    EuropeanClub = 3
}
